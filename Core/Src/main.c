void USBH_HID_EventCallback(USBH_HandleTypeDef *phost) {
	if (USBH_HID_GetDeviceType(phost) == HID_KEYBOARD) {
		HID_KEYBD_Info_TypeDef *Keybd_data;
		Keybd_data = USBH_HID_GetKeybdInfo(phost);
		char Key = USBH_HID_GetASCIICode(Keybd_data);
		printf("Key pressed: %c \n", Key);
	}
	else if (USBH_HID_GetDeviceType(phost) == HID_MOUSE) {
		HID_MOUSE_Info_TypeDef *Mouse_data;
		Mouse_data = USBH_HID_GetMouseInfo(phost);
		printf("X: %d, Y: %d, Button1: %d, Button2: %d, Button3: %d \n", Mouse_data->x,Mouse_data->y, \
				Mouse_data->buttons[0], Mouse_data->buttons[1], Mouse_data->buttons[2]);
	}
	else if (USBH_HID_GetDeviceType(phost) == HID_UNKNOWN) {
		printf("USB Unknown! \n");
	}
}
